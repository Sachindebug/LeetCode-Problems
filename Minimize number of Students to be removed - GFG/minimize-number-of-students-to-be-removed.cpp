class Solution {
  public:
    int lengthOfLIS(int nums[],int n){
        vector<int> res;
        for(int i=0; i<n; i++) {
            auto it = std::lower_bound(res.begin(), res.end(), nums[i]);
            if(it==res.end()) res.push_back(nums[i]);
            else *it = nums[i];
        }
        return res.size();
    }
    int removeStudents(int H[], int N) {
        // code here
        return N-lengthOfLIS(H,N);
    }
};

